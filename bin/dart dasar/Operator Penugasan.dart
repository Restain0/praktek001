void main() {
  int a = 10;
  // $ digunkan untuk menampilkan nilai variabel
  //di dalam string sesuai dengan materi expression
  print("Nilai awal a = $a");

  a += 5;
  print("Setelah a += 5  ->");

  a -= 3;
  print("Setelah a -= 3  -> $a");

  a *= 2;
  print("Setelah a *= 2  -> a");

  a ~/= 4;
  print("Setelah a ~/= 4 -> $a");

  a %= 3;
  print("Setelah a %= 3  -> $a");
}
